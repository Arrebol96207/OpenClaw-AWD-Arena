def parse_cookies(cookie_header):
    cookies = {}
    for raw_part in cookie_header.split(";"):
        part = raw_part.strip()
        if not part or "=" not in part:
            continue
        key, value = part.split("=", 1)
        cookies[key] = value
    return cookies


